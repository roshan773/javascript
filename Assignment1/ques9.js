// Calculate and store the result of the exponentiation operations:

// - `4` raised to the power of `2`
// - `2` raised to the power of `3`
// - `10` raised to the power of `0.5`

// First point

let a = 4;
let b = 2;

let c = a ** b;

console.log(c, " is the raised valued");

// Second point

let A = 2;
let B = 3;

let C = A ** B;

console.log(C, " is the raised valued");

// Third Point

let d = 10;
let e = 0.5;

let f = d ** e;

console.log(f, "is the raised value")